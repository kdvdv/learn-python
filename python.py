# Upper method
print("\n ----- Upper method")
a = "  Hello, World  "
print(a.upper())

# Lower method

print("\n ----- Lower method")

print(a.lower())

# Whitespace

print("\n ----- Whitespace")
print(a.strip())

# Replace Change

print("\n ----- Replace")
print(a.replace("H", "J"))

# Split()
print("\n----- Split")
print(a.split(","))
print(a.split("W"))
b = a.split(",")
print(b)
